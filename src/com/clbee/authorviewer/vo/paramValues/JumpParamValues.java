package com.clbee.authorviewer.vo.paramValues;

public class JumpParamValues {
	String mDelay;
	String mDuration;
	String mReverse;
	String mWaitingTime; 
	String mRevDuration;
	String mRepeatCount;
	String mRepeatForever;
	String mOpacity;
	String mPosition;
	String mRevPosition;
	String mJumpCount;
	String mJjumpHeight;
	public JumpParamValues(String mDelay, String mDuration, String mReverse,
			String mWaitingTime, String mRevDuration, String mRepeatCount,
			String mRepeatForever, String mOpacity, String mPosition,
			String mRevPosition, String mJumpCount, String mJjumpHeight) {
		super();
		this.mDelay = mDelay;
		this.mDuration = mDuration;
		this.mReverse = mReverse;
		this.mWaitingTime = mWaitingTime;
		this.mRevDuration = mRevDuration;
		this.mRepeatCount = mRepeatCount;
		this.mRepeatForever = mRepeatForever;
		this.mOpacity = mOpacity;
		this.mPosition = mPosition;
		this.mRevPosition = mRevPosition;
		this.mJumpCount = mJumpCount;
		this.mJjumpHeight = mJjumpHeight;
	}
	public String getmDelay() {
		return mDelay;
	}
	public void setmDelay(String mDelay) {
		this.mDelay = mDelay;
	}
	public String getmDuration() {
		return mDuration;
	}
	public void setmDuration(String mDuration) {
		this.mDuration = mDuration;
	}
	public String getmReverse() {
		return mReverse;
	}
	public void setmReverse(String mReverse) {
		this.mReverse = mReverse;
	}
	public String getmWaitingTime() {
		return mWaitingTime;
	}
	public void setmWaitingTime(String mWaitingTime) {
		this.mWaitingTime = mWaitingTime;
	}
	public String getmRevDuration() {
		return mRevDuration;
	}
	public void setmRevDuration(String mRevDuration) {
		this.mRevDuration = mRevDuration;
	}
	public String getmRepeatCount() {
		return mRepeatCount;
	}
	public void setmRepeatCount(String mRepeatCount) {
		this.mRepeatCount = mRepeatCount;
	}
	public String getmRepeatForever() {
		return mRepeatForever;
	}
	public void setmRepeatForever(String mRepeatForever) {
		this.mRepeatForever = mRepeatForever;
	}
	public String getmOpacity() {
		return mOpacity;
	}
	public void setmOpacity(String mOpacity) {
		this.mOpacity = mOpacity;
	}
	public String getmPosition() {
		return mPosition;
	}
	public void setmPosition(String mPosition) {
		this.mPosition = mPosition;
	}
	public String getmRevPosition() {
		return mRevPosition;
	}
	public void setmRevPosition(String mRevPosition) {
		this.mRevPosition = mRevPosition;
	}
	public String getmJumpCount() {
		return mJumpCount;
	}
	public void setmJumpCount(String mJumpCount) {
		this.mJumpCount = mJumpCount;
	}
	public String getmJjumpHeight() {
		return mJjumpHeight;
	}
	public void setmJjumpHeight(String mJjumpHeight) {
		this.mJjumpHeight = mJjumpHeight;
	}
}
