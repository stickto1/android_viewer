package com.clbee.authorviewer.vo.paramValues;

public class ScaleMoveParamValues {
	String delay;
	String duration;
	String reverse;
	String waitingTime; 
	String revDuration;
	String repeatCount;
	String repeatForever;
	String opacity;
	String position;
	String revPosition;
	String scaleWidth;
	String scaleHeight;
	String scaleX;
	String scaleY;
	String revScaleWidth;
	String revScaleHeight;
	String revScaleX;
	String revScaleY;
	public ScaleMoveParamValues(String delay, String duration, String reverse,
			String waitingTime, String revDuration, String repeatCount,
			String repeatForever, String opacity, String position,
			String revPosition, String scaleWidth, String scaleHeight,
			String scaleX, String scaleY, String revScaleWidth,
			String revScaleHeight, String revScaleX, String revScaleY) {
		super();
		this.delay = delay;
		this.duration = duration;
		this.reverse = reverse;
		this.waitingTime = waitingTime;
		this.revDuration = revDuration;
		this.repeatCount = repeatCount;
		this.repeatForever = repeatForever;
		this.opacity = opacity;
		this.position = position;
		this.revPosition = revPosition;
		this.scaleWidth = scaleWidth;
		this.scaleHeight = scaleHeight;
		this.scaleX = scaleX;
		this.scaleY = scaleY;
		this.revScaleWidth = revScaleWidth;
		this.revScaleHeight = revScaleHeight;
		this.revScaleX = revScaleX;
		this.revScaleY = revScaleY;
	}
	

}
