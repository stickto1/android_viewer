package com.clbee.authorviewer.vo.paramValues;

public class RotateParamValues {
	String mDelay;
	String mDuration;
	String mReverse;
	String mWaitingTime; 
	String mRevDuration;
	String mRepeatCount;
	String mRepeatForever;
	String mOpacity;
	String mAngle;
	String mRevAngle;
	String mRevAnchorPoint;
	public RotateParamValues(String mDelay, String mDuration, String mReverse,
			String mWaitingTime, String mRevDuration, String mRepeatCount,
			String mRepeatForever, String mOpacity, String mAngle,
			String mRevAngle, String mRevAnchorPoint) {
		super();
		this.mDelay = mDelay;
		this.mDuration = mDuration;
		this.mReverse = mReverse;
		this.mWaitingTime = mWaitingTime;
		this.mRevDuration = mRevDuration;
		this.mRepeatCount = mRepeatCount;
		this.mRepeatForever = mRepeatForever;
		this.mOpacity = mOpacity;
		this.mAngle = mAngle;
		this.mRevAngle = mRevAngle;
		this.mRevAnchorPoint = mRevAnchorPoint;
	}
	public String getmDelay() {
		return mDelay;
	}
	public void setmDelay(String mDelay) {
		this.mDelay = mDelay;
	}
	public String getmDuration() {
		return mDuration;
	}
	public void setmDuration(String mDuration) {
		this.mDuration = mDuration;
	}
	public String getmReverse() {
		return mReverse;
	}
	public void setmReverse(String mReverse) {
		this.mReverse = mReverse;
	}
	public String getmWaitingTime() {
		return mWaitingTime;
	}
	public void setmWaitingTime(String mWaitingTime) {
		this.mWaitingTime = mWaitingTime;
	}
	public String getmRevDuration() {
		return mRevDuration;
	}
	public void setmRevDuration(String mRevDuration) {
		this.mRevDuration = mRevDuration;
	}
	public String getmRepeatCount() {
		return mRepeatCount;
	}
	public void setmRepeatCount(String mRepeatCount) {
		this.mRepeatCount = mRepeatCount;
	}
	public String getmRepeatForever() {
		return mRepeatForever;
	}
	public void setmRepeatForever(String mRepeatForever) {
		this.mRepeatForever = mRepeatForever;
	}
	public String getmOpacity() {
		return mOpacity;
	}
	public void setmOpacity(String mOpacity) {
		this.mOpacity = mOpacity;
	}
	public String getmAngle() {
		return mAngle;
	}
	public void setmAngle(String mAngle) {
		this.mAngle = mAngle;
	}
	public String getmRevAngle() {
		return mRevAngle;
	}
	public void setmRevAngle(String mRevAngle) {
		this.mRevAngle = mRevAngle;
	}
	public String getmRevAnchorPoint() {
		return mRevAnchorPoint;
	}
	public void setmRevAnchorPoint(String mRevAnchorPoint) {
		this.mRevAnchorPoint = mRevAnchorPoint;
	}
}
